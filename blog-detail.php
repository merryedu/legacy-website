                                    <h3> 每个优秀孩子背后都有一个用心的家长 </h3>
                                </div>			
                                <!--entry-metadata ends-->	
                                <div class="entry-body">
                                    <p>因为经常和班上的家长沟通，很有幸结识了很多牛家长。这些孩子不但有好的学习成绩，还都拥有良好的学习习惯、阳光的性格，甚至是多才多艺、爱好广泛。每个孩子在成长的过程中，都会遇到各种各样的问题。学习习惯不好、学习成绩不好、脾气大、顶嘴……，似乎孩子们拥有了太多的问题。孩子，你怎么会长成这样？你是不是也会这样想？如果这样，那你就错了！</p>
                                    </blockquote>
                                    <p>在问责孩子的时候，作为家长的你，有没有问责自己呢？</p>
                                    <p>孩子的学习习惯不好，作为家长有没有要求过孩子要自己的事情自己做？你会说：“我要求了！”也许你真的要求了，但是你有没认真的执行呢？有没有只是嘴上说着自己的事情自己做，手上确帮孩子削着铅笔，收拾着书包呢？也许，削铅笔收书包都是小事情，但是就是这些小事无形中让孩子的学习依赖情绪产生，并滋长。</p>
                                    <p>孩子的的成绩不理想时，你是一味的发火责难，还是帮助孩子分析试卷找出问题？孩子学习时，你是创造安静的环境，还是在一旁看电视嗑瓜子？孩子取得成绩时，你是盲目的物质奖励了事，还是适度的赞扬？孩子不听你的意见，大发脾气的时候，你又没有想到你也曾经没有用心的倾听孩子的意见，你也用发脾气来解决问题？</p>
                                    <p>学习，不应该是孩子一个人独立去面对的事情</p>
                                    <p>在我看来，学习是一个综合事件，它包括学习态度、学习习惯、学习方法、学习成绩。而学习成绩的好坏是这几个方面的表面现象。作为家长，应该协助孩子去完成这个综合而复杂的事情。</p>
                                    <blockquote class="alignright">
                                        <q>和班上一个牛娃的家长聊天，我也同样受益匪浅，分享给想要孩子进步的家长们~~~</q>
                                    </blockquote>
                                    <p>这位家长这样说：</p>
                                    
                                    <p>1、在学习态度上，我希望孩子们明白学习是一生持续的，人生的每个阶段都有不同的学习任务。而学习，也不只限制在语文数学英语上，学习的内涵很大。正所谓胸怀宽广、放眼世界。所以看书成为孩子娱乐的一种方式，去图书馆和去游乐园一样的开心，“六一”儿童节，所有朋友同学同事送给孩子的礼物只收书，其他一律不接受。</p>
                                    <p>2、在学习习惯上，更是要严格要求。学习计划表是个很好的选择，每月的学习任务，要明确的分配到每周，每日又要有计划的执行。同时，根据每个月的实际情况进行一些调整。这样一份切实可行的学习计划表。同时大人们必须说话算数，千万不可以动摇，说好周一到周四不准看电视，就一眼都不能看，除非学习计划表上的事已经超前完成，但是也只能看20分钟。</p>
                                    <p>3、学习方法上，首先家长应该清楚的知道自己孩子的优点和缺点，不能盲目的护短。很多家长都把孩子学习上的错误归结为“马虎”。出现错题的时候，要分析是假“马虎”还是真不会。比如计算，有的时候表现出来是孩子看错了符号或者结果错了，但是实际上可能就是孩子掌握的不够熟练。真的熟练了不会算错也不会看错。对于不会的题目，家长要帮助孩子进行学习，不要一下子告诉孩子解题的思路和答案，要引导着孩子去考虑题目。如果实在给孩子讲不明白，一定及早向老师求助。不要担心面子问题。有了端正的学习态度、良好的学习习惯、正确的学习方法，学习成绩自然优秀。</p>
                                    <p>4、情商上，这位家长和我说，孩子过于争强好胜，导致在班级的群众基础不是很好，但大家都知道进入社会不完全看成绩，情商也很重要，所以发现孩子这个缺点后，开始有意培养孩子，让他变得更加的大气。于是规定每位到家里的客人在离开的时候都要孩子送给他一件礼物，绝不能空手而归，规定一直在遵守，虽然孩子开始都是送的些对自己来说无关紧要的。</p>
                                    <p>说这么多不是让您模仿其他家长是如何做的，我是想告诉各位家长，孩子不可能十全十美，我们需要帮助孩子找到缺点，找到问题所在，用心去分析孩子的问题，不要抱怨自己家的孩子哪些方面不如其他孩子，没有无缘无故的优秀，你只看到了其他优秀的孩子，殊不知这些优秀的背后是他们父母更多的心血和努力。培养孩子是一项事业，只有用心才能帮助孩子取长补短，才能让孩子变得更加优秀。</p>
